     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function edit($id)
    {
        $categories=Category::pluck('name','id')->all();

        $intity=intity::findOrFail($id);
        return view('panel.modify',compact('intity','categories'));
    }

    /**
